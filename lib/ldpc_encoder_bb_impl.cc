    void
    ldpc_encoder_bb_impl::forecast (int noutput_items, gr_vector_int &ninput_items_required)
    {
        ninput_items_required[0] = (noutput_items / frame_size) * nouter;
    }

/*
 * Codificaci√≥n tipo B o IRA:
 * q(i,j,l)=q(i,j,0)+Qldpc*l(mod Minner), para l de 0 a 360
 * l=k(mod 360); se hace 0 en 360 y multiplos de 360, de tal forma que cuando se cambia de fila se toman las direcciones tal y como vienen en la tabla
 * i=|_k/360_|; es cero hasta que k=360, cambia a una nueva fila cada 360 datos
 */

#define LDPC_BF(TABLE_COD, ROWS) \
for (int row = 0; row < ROWS; row++) \
{ \
    for(int l = 0; l < 360; l++) \
    { \
        for (int col = 1; col <= TABLE_COD[row][0]; col++) \
        { \
            ldpc_encode.p[index] = (TABLE_COD[row][col] + (l * q)) % pbits; \
            ldpc_encode.d[index] = im; \
            index++; \
        } \
        im++; \
    } \
}

void ldpc_encoder_bb_impl::ldpc_lookup_generate()
{
    int im;
    int index;
    int pbits;
    int q;
    index = 0;
    im = 0;

    pbits = frame_size - nouter;    //number of parity bits
    q = q_val;

    if (frame_size == FRAME_SIZE_NORMAL)
    {
        cout << ">>> Code length: Normal" << endl;
        if (code_rate == gr::atsc32::C6_15) {
            LDPC_BF(ldpc_tab_6_15N, 72);
            cout << "Encoding at 6/15" << endl;
        }

        if (code_rate == gr::atsc32::C8_15)  {
            LDPC_BF(ldpc_tab_8_15N, 96);
            cout << "Encoding at 8/15" << endl;
        }

        if (code_rate == gr::atsc32::C9_15)  {
            LDPC_BF(ldpc_tab_9_15N, 108);
            cout << "Encoding at 9/15" << endl;
        }

        if (code_rate == gr::atsc32::C10_15)  {
            LDPC_BF(ldpc_tab_10_15N, 120);
            cout << "Encoding at 10/15" << endl;
        }

        if (code_rate == gr::atsc32::C11_15)  {
            LDPC_BF(ldpc_tab_11_15N, 132);
            cout << "Encoding at 11/15" << endl;
        }
    }
    else
    {
        cout << ">>> Code length: Short" << endl;
        if (code_rate == gr::atsc32::C6_15) {
            LDPC_BF(ldpc_tab_6_15S, 18);
            cout << "Encoding at 6/15" << endl;
        }

        if (code_rate == gr::atsc32::C7_15) {
            LDPC_BF(ldpc_tab_7_15S, 21);
            cout << "Encoding at 7/15" << endl;
        }
        if (code_rate == gr::atsc32::C8_15)  {
            LDPC_BF(ldpc_tab_8_15S, 24);
            cout << "Encoding at 8/15" << endl;
        }
        if (code_rate == gr::atsc32::C9_15)  {
            LDPC_BF(ldpc_tab_9_15S, 27);
            cout << "Encoding at 9/15" << endl;
        }

        if (code_rate == gr::atsc32::C10_15)  {
            LDPC_BF(ldpc_tab_10_15S, 30);
            cout << "Encoding at 10/15" << endl;
        }

        if (code_rate == gr::atsc32::C11_15)  {
            LDPC_BF(ldpc_tab_11_15S, 33);
            cout << "Encoding at 11/15" << endl;
        }
    }
    ldpc_encode.table_length = index;
}

    int
    ldpc_encoder_bb_impl::general_work (int noutput_items,
                       gr_vector_int &ninput_items,
                       gr_vector_const_void_star &input_items,
                       gr_vector_void_star &output_items)
    {
      const unsigned char *in = (const unsigned char *) input_items[0];
      unsigned char *out = (unsigned char *) output_items[0];
      const unsigned char *d;
      unsigned char *p;
      //numero de bits de paridad
      int plen = frame_size - nouter;
      d = in;
      p = &out[nouter];
      int consumed = 0;

        for (int i = 0; i < noutput_items; i += frame_size)
        {
            // First zero all the parity bits
            memset(p, 0, sizeof(unsigned char)*plen);
            for(int j = 0; j < (int)nouter; j++)
            {
                out[i + j] = in[consumed];
                consumed++;
            }
            // now do the parity checking
            for(int j = 0; j < ldpc_encode.table_length; j++)
            {
                p[ldpc_encode.p[j]] ^= d[ldpc_encode.d[j]];
            }
            for(int j = 1; j < plen; j++)
            {
                p[j] ^= p[j-1];
            }
            d += nouter;
            p += frame_size;
        }
      // Tell runtime system how many input items we consumed on
      // each input stream.
      consume_each (consumed);

      // Tell runtime system how many output items we produced.
      return noutput_items;
    }

