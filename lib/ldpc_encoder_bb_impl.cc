    ldpc_encoder_bb::sptr
    ldpc_encoder_bb::make(atsc3_framesize_t framesize, atsc3_code_rate_t rate)
    {
      return gnuradio::get_initial_sptr
        (new ldpc_encoder_bb_impl(framesize, rate));
    }

    /*
     * The private constructor
     */
    ldpc_encoder_bb_impl::ldpc_encoder_bb_impl(atsc3_framesize_t framesize, atsc3_code_rate_t rate)
      : gr::block("ldpc_encoder_bb",
              gr::io_signature::make(1, 1, sizeof(unsigned char)),
              gr::io_signature::make(1, 1, sizeof(unsigned char)))
    {
        if (framesize == gr::atsc32::FECFRAME_NORMAL)
        {
            frame_size = FRAME_SIZE_NORMAL;
            switch (rate)
            {
                case gr::atsc32::C6_15:
                    nouter = 25920;
                    q_val = 108;
                    break;

                case gr::atsc32::C8_15:
                    nouter = 34560;
                    q_val = 84;
                    break;

                case gr::atsc32::C9_15:
                    nouter = 38880;
                    q_val = 72;
                    break;

                case gr::atsc32::C10_15:
                    nouter = 43200;
                    q_val = 60;
                    break;

                case gr::atsc32::C11_15:
                    nouter = 47520;
                    q_val = 48;
                    break;
            }

        }
        else
        {
            frame_size = FRAME_SIZE_SHORT;
            switch (rate)
            {
                case gr::atsc32::C6_15:
                    nouter = 6480;
                    q_val = 27;
                    break;

                case gr::atsc32::C7_15:
                    nouter = 7560;
                    q_val = 24;
                    break;

                case gr::atsc32::C8_15:
                    nouter = 8640;
                    q_val = 21;
                    break;

                case gr::atsc32::C9_15:
                    nouter = 9720;
                    q_val = 18;
                    break;

                case gr::atsc32::C10_15:
                    nouter = 10800;
                    q_val = 15;
                    break;

                case gr::atsc32::C11_15:
                    nouter = 11880;
                    q_val = 12;
                    break;
            }
        }
        code_rate = rate;
        ldpc_lookup_generate();
        set_output_multiple(frame_size);
    }

